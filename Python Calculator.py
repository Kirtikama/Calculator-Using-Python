print("Calculator")
num1= int(input("enter number1  "))
num2= int(input("enter number2  "))
operator= input("Please enter a operator + - * / % ** //")
if operator =="+":
    print("the sum of two number is",num1+num2)
if operator =="-":
    print("the sum of two number is",num1-num2)
if operator == "*":
    print("the sum of two number is", num1*num2)
if operator == "/":
    print("the sum of two number is", num1/num2)
if operator == "%":
    print("the sum of two number is", num1%num2)
if operator == "**":
    print("the sum of two number is", num1**num2)
if operator == "//":
    print("the sum of two number is", num1//num2)